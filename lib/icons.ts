export function getIconForLanguageExtension(
  language: string
): React.ReactElement | null {
  switch (language) {
    case "json":
      return React.createElement(JsonIcon)
    case "ico":
      return React.createElement(IcoIcon)
    case "svg":
      return React.createElement(SvgIcon)
    case "mjs":
    case "js":
    case "jsx":
      return React.createElement(JsIcon)
    case "css":
      return React.createElement(CssIcon, { className: "fill-foreground" })
    case "gitignore":
      return React.createElement(GitignoreIcon)
    case "ts":
    case "typescript":
      return React.createElement(TsIcon, { className: "fill-foreground" })
    case "tsx":
      return React.createElement(TsxIcon, { className: "fill-foreground" })
    case "md":
    case "markdown":
      return React.createElement(MdIcon)
    default:
      return null
  }
}
